
#include "velocity.h"

/*************************************
 * Velocity - initializer
 **************************************/
Velocity::Velocity()
{
   dx = 0;
   dy = 0;
}

/*************************************
 * Velocity - custom initializer
 **************************************/
Velocity::Velocity(float x, float y)
{
   dx = x;
   dy = y;
}

/*************************************
 * get dx
 **************************************/
float Velocity::getDx()
{
   return(dx);
}

/*************************************
 * get dy
 **************************************/
float Velocity::getDy()
{
   return(dy);
}

/*************************************
 * set dx
 **************************************/
void Velocity::setDx(float x)
{
   dx = x;
}

/*************************************
 * set dy
 **************************************/
void Velocity::setDy(float y)
{
   dy = y;
}

/*************************************
 * add dy
 **************************************/
void Velocity::addDy(float y)
{
   dy += y;
}

/*************************************
 * add dx
 **************************************/
void Velocity::addDx(float x)
{
   dx += x;
}
